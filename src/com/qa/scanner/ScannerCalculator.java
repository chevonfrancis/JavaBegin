package com.qa.scanner;

public class ScannerCalculator {

		
		public int add(int a, int b) {
			return a + b;
		}
		
		public int sub(int a, int b) {
			return a - b;
		}
		
		public int mul(int a, int b) {
			return a * b;
		}
		
		
		public double div1(int a, int b) {
			double x = a;
			double y = b;
			return x / y;
		}
	}


